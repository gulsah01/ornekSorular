package temmuz19_while_loop;

public class slayt150_soru1 {
    public static void main(String[] args) {

        //Soru 1- While loop kullanarak 2 basamakli 7 ile bolunebilen
        // pozitif tamsayilar yazdirin.

       int num=10;
       while (num<100){
           if (num%7==0){
               System.out.println("Iki basamakli 7 ile bolunen sayilar :"+num);
           }
           num++;
       }





    }
}